String formatDate(String? dateStr) {
  if (dateStr == null || dateStr.isEmpty) return '';
  final parts = dateStr.split("-");
  if (parts.length != 3) return dateStr;
  return "${parts[2]}-${parts[1]}-${parts[0]}";
}
