    final storage = FlutterSecureStorage();
    String? userId = await storage.read(key: 'userId');
    if (userId == null) {
      final prefs = await SharedPreferences.getInstance();
      userId = prefs.getString('userId');
      print('Fallback to SharedPreferences userId: $userId');
    }
    print('Submitting KYC for userId: $userId and token: 8dbd9a450b5aaeba7225a58b86b35420');
    final uri = Uri.parse('https://msebeccs.com/API/kycCheckUpdate.php');
    Map<String, dynamic> body = {
      'userId': userId,
      'token': '8dbd9a450b5aaeba7225a58b86b35420',
    };

    _textControllers.forEach((key, controller) {
      body[key] = controller.text;
    });

    if (gender != null) body['gender'] = gender![0];
    if (designation != null) body['designation'] = reverseDesignationMap[designation!] ?? '';
    if (division != null) {
      body['division'] = divisionOptions.entries.firstWhere(
            (e) => e.value == division,
        orElse: () => MapEntry('', ''),
      ).key;
    }
    if (nominee1Relation != null) body['nominee1Relation'] = reverseNomineeRelationMap[nominee1Relation!] ?? '';
    if (nominee2Relation != null) body['nominee2Relation'] = reverseNomineeRelationMap[nominee2Relation!] ?? '';
    if (selectedCurrentStateId != null) body['current_state'] = selectedCurrentStateId;
    if (selectedPermanentStateId != null) body['permanent_state'] = selectedPermanentStateId;
    if (bloodGroup != null) body['bloodGroup'] = bloodGroup!;
    // Encode images
    final aadhaarBytes = await aadhaarImage!.readAsBytes();
    final panBytes = await panImage!.readAsBytes();
    final profileBytes = await profileImage!.readAsBytes();
    final signatureBytes = await signatureImage!.readAsBytes();

    body['aadhaar_image'] = base64Encode(aadhaarBytes);
    body['pan_image'] = base64Encode(panBytes);
    body['profile_photo'] = base64Encode(profileBytes);
    body['signature_image'] = base64Encode(signatureBytes);
    try {
      final response = await http.post(
        uri,
        headers: {'Content-Type': 'application/json'},
        body: jsonEncode(body),
      );
      if (response.statusCode == 200) {
        final trimmedBody = response.body.trim();
        if (trimmedBody.isEmpty) {
          // Treat this as a special success fallback, if expected
          ScaffoldMessenger.of(context).showSnackBar(
            const SnackBar(content: Text('KYC submitted successfully.')),
          );
        } else {
          final json = jsonDecode(trimmedBody);
          if (json['success'] == 'true' || json['success'] == true || json['success'] == '1') {
            ScaffoldMessenger.of(context).showSnackBar(
              const SnackBar(content: Text('KYC submitted successfully')),
            );
          } else {
            ScaffoldMessenger.of(context).showSnackBar(
              SnackBar(content: Text('Submission failed: ${json['message'] ?? 'Unknown error'}')),
            );
          }
        }
      } else {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Server error: ${response.statusCode}')),
        );
      }
    } catch (e, st) {
      print('Exception during submission: $e\n$st');
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Something went wrong: $e')),
      );
    }
    finally {
      setState(() => isLoading = false);
    }
  }


  Future<void> pickImage(Function(File) onPicked) async {
    final pickedFile = await _picker.pickImage(source: ImageSource.gallery);
    if (pickedFile != null) {
      File originalFile = File(pickedFile.path);
      Uint8List compressedBytes = await compressImageToTargetSize(originalFile);

      // Save compressed bytes as a temp file
      final tempDir = await getTemporaryDirectory();
      final compressedFile = await File('${tempDir.path}/${DateTime.now().millisecondsSinceEpoch}.jpg').writeAsBytes(compressedBytes);

      onPicked(compressedFile);
    }
  }

