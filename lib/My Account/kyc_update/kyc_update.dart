  Future<Uint8List> compressImageToTargetSize(File file, {int maxSizeInBytes = 200 * 1024}) async {
    final image = img.decodeImage(await file.readAsBytes());
    if (image == null) throw Exception('Invalid image');

    int quality = 100;
    late Uint8List encoded;
    // Try compressing until it's under 200KB
    do {
      encoded = Uint8List.fromList(img.encodeJpg(image, quality: quality));
      quality -= 5;
      if (quality <= 10) break; // Prevent infinite loop or too poor quality
    } while (encoded.lengthInBytes > maxSizeInBytes);

    return encoded;
  }
